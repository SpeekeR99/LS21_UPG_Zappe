/**
 * Trida predstavuje prepravku souradnic X, Y
 *
 * @author Dominik Zappe
 * @version 2.2.0
 */
public class Pozice {

    /** souradnice X */
    public double x;
    /** souradnice Y */
    public double y;

    /**
     * Konstruktor priradi souradnice X a Y
     * @param x souradnice X
     * @param y souradnice Y
     */
    public Pozice(double x, double y) {
        this.x = x;
        this.y = y;
    }

}
